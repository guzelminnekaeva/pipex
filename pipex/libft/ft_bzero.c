#include <stddef.h>

void	ft_bzero(void	*s, size_t	n)
{
	size_t			i;
	unsigned char	*m;

	m = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		*m++ = '\0';
		i++;
	}
}
